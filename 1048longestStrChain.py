# Longest String Chain
# You are given an array of words where each word is a string of lowercase letters.
# worda is a predecessor of wordb if and only if we can insert exactly one letter anywhere in worda
# without changing the order of the other characters to make it equal to wordb.
#
# For example, "abc" is a predecessor of "abac", while "cba" is not a predecessor of "bcad".
#
# A word chain is a sequence of words [word_1, word_2, ..., word_k] with k >= 1,
# where word_1 is a predecessor of word_2, word_2 is a predecessor of word_3, and so on.
# A single word is trivially a word chain with k = 1.
#
# Return the length of the longest word chain containing words chosen from the given array of words.


